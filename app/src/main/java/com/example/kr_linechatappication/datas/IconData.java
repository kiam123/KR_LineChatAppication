package com.example.kr_linechatappication.datas;

public class IconData {
    String mImageIcon;
    int view;

    public IconData(String imageIcon, int view) {
        this.mImageIcon = imageIcon;
        this.view = view;
    }

    public String getmImageIcon() {
        return mImageIcon;
    }

    public void setmImageIcon(String mImageIcon) {
        this.mImageIcon = mImageIcon;
    }

    public int getView() {
        return view;
    }

    public void setView(int view) {
        this.view = view;
    }
}
